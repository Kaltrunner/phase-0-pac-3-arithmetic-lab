function add(a, b) {
    return a + b;
}
function subtract(c, d) {
    return c - d;
}
function multiply(e, f) {
    return e * f;
}
function divide(g, h) {
    return g / h;
}

function makeInt(n) {
    return parseInt(n, 10)
}

function preserveDecimal(string) {
    return parseFloat(string)
}

function increment(n) {
    return n + 1;
}

function decrement(n) {
    return n - 1;
}